CREATE OR REPLACE VIEW contabilidad.v_transaccion_detalle_venta AS
SELECT
  dv.*,
  t.fecha_transaccion,
  t.tipo_transaccion,
  t.glosa,
  pe.nombre AS producto_educativo_nombre,
  b.nombre AS producto_tienda_nombre,
  cli.nombres AS cliente_nombres,
  cli.apellidos AS cliente_apellidos,
  ci.codigo AS cuenta_ingreso_codigo,
  ci.nombre_cuenta AS cuenta_ingreso_nombre
FROM contabilidad.transaccion_detalle_venta dv
JOIN contabilidad.transaccion t ON t.id_transaccion = dv.id_transaccion
LEFT JOIN servicios_educativos.producto_educativo pe ON pe.id_producto_educativo = dv.id_producto_educativo
LEFT JOIN inventario.bien b ON b.id_bien = dv.id_producto_tienda
LEFT JOIN persona.persona cli ON cli.id_persona = dv.id_cliente
LEFT JOIN contabilidad.cuenta ci ON ci.id_cuenta = dv.id_cuenta_ingreso
WHERE dv.estado_registro = 'Activo';

CREATE OR REPLACE VIEW contabilidad.v_transaccion_detalle_costo AS
SELECT
  dc.*,
  t.fecha_transaccion,
  t.tipo_transaccion,
  t.glosa,
  cc.codigo AS centro_costo_codigo,
  cc.nombre AS centro_costo_nombre,
  cco.codigo AS concepto_costo_codigo,
  cco.nombre AS concepto_costo_nombre,
  p.nombre_proveedor,
  b.nombre AS bien_nombre,
  ccu.codigo AS cuenta_costo_codigo,
  ccu.nombre_cuenta AS cuenta_costo_nombre
FROM contabilidad.transaccion_detalle_costo dc
JOIN contabilidad.transaccion t ON t.id_transaccion = dc.id_transaccion
LEFT JOIN contabilidad.centro_costo cc ON cc.id_centro_costo = dc.id_centro_costo
LEFT JOIN contabilidad.concepto_costo cco ON cco.id_concepto = dc.id_concepto_costo
LEFT JOIN persona.proveedor p ON p.id_proveedor = dc.id_proveedor
LEFT JOIN inventario.bien b ON b.id_bien = dc.id_bien
LEFT JOIN contabilidad.cuenta ccu ON ccu.id_cuenta = dc.id_cuenta_costo
WHERE dc.estado_registro = 'Activo';

