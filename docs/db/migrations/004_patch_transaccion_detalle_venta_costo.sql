CREATE OR REPLACE FUNCTION contabilidad.fn_validar_detalle_transaccion_tipo()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  tipo_esperado text := TG_ARGV[0];
  tipo_actual text;
BEGIN
  SELECT t.tipo_transaccion::text
    INTO tipo_actual
  FROM contabilidad.transaccion t
  WHERE t.id_transaccion = NEW.id_transaccion;

  IF tipo_actual IS NULL THEN
    RAISE EXCEPTION 'No existe la transacción % para registrar el detalle.', NEW.id_transaccion;
  END IF;

  IF tipo_actual <> tipo_esperado THEN
    RAISE EXCEPTION 'La tabla % solo acepta transacciones tipo %, pero recibió tipo %.', TG_TABLE_NAME, tipo_esperado, tipo_actual;
  END IF;

  RETURN NEW;
END;
$$;

