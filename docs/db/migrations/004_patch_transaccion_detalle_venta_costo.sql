-- 004_patch_transaccion_detalle_venta_costo.sql
-- Patch contable: detalle operativo para transacciones de VENTA y COSTO.
-- Permite que una transacción contable tenga detalle comercial/de costo separado
-- de sus movimientos de debe/haber en contabilidad.transaccion_movimiento_cuenta.

CREATE TABLE IF NOT EXISTS contabilidad.transaccion_detalle_venta (
    id_detalle_venta bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_transaccion bigint NOT NULL,
    numero_linea integer NOT NULL DEFAULT 1,
    id_cliente bigint,
    id_producto_educativo bigint,
    id_producto_tienda bigint,
    id_curso_version bigint,
    id_clase_por_hora bigint,
    id_tienda bigint,
    id_sucursal bigint,
    id_cuenta_ingreso bigint,
    descripcion character varying(300),
    cantidad numeric(18,6) NOT NULL DEFAULT 1,
    precio_unitario numeric(18,6) NOT NULL DEFAULT 0,
    porcentaje_descuento numeric(9,6) NOT NULL DEFAULT 0,
    monto_descuento numeric(18,6) NOT NULL DEFAULT 0,
    monto_subtotal numeric(18,6) NOT NULL DEFAULT 0,
    porcentaje_impuesto numeric(9,6) NOT NULL DEFAULT 0,
    monto_impuesto numeric(18,6) NOT NULL DEFAULT 0,
    monto_total numeric(18,6) NOT NULL DEFAULT 0,
    moneda character varying(3) NOT NULL DEFAULT 'BOB',
    observaciones text,
    estado_registro character varying(20) DEFAULT 'Activo'::character varying,
    fecha_registro timestamp with time zone DEFAULT now(),
    fecha_modificacion timestamp with time zone,
    version_registro integer DEFAULT 1,
    id_usuario_creador bigint,
    id_usuario_modificacion bigint,
    CONSTRAINT ck_transaccion_detalle_venta_cantidad CHECK (cantidad > 0),
    CONSTRAINT ck_transaccion_detalle_venta_precio CHECK (precio_unitario >= 0),
    CONSTRAINT ck_transaccion_detalle_venta_descuento CHECK (porcentaje_descuento >= 0 AND porcentaje_descuento <= 100),
    CONSTRAINT ck_transaccion_detalle_venta_impuesto CHECK (porcentaje_impuesto >= 0 AND porcentaje_impuesto <= 100),
    CONSTRAINT ck_transaccion_detalle_venta_montos CHECK (
      monto_descuento >= 0 AND monto_subtotal >= 0 AND monto_impuesto >= 0 AND monto_total >= 0
    ),
    CONSTRAINT ck_transaccion_detalle_venta_producto CHECK (
      id_producto_educativo IS NOT NULL
      OR id_producto_tienda IS NOT NULL
      OR id_curso_version IS NOT NULL
      OR id_clase_por_hora IS NOT NULL
      OR descripcion IS NOT NULL
    )
);

